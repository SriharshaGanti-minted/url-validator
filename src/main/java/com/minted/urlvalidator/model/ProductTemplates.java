package com.minted.urlvalidator.model;

public class ProductTemplates {
	
	private String productSku;
	private String skinnywrapTemplate;
	private String linerTemplate;
	private String rapTemplate; 
	private String rcpTemplate;
	public String getProductSku() {
		return productSku;
	}
	public void setProductSku(String productSku) {
		this.productSku = productSku;
	}
	public String getSkinnywrapTemplate() {
		return skinnywrapTemplate;
	}
	public void setSkinnywrapTemplate(String skinnywrapTemplate) {
		this.skinnywrapTemplate = skinnywrapTemplate;
	}
	public String getLinerTemplate() {
		return linerTemplate;
	}
	public void setLinerTemplate(String linerTemplate) {
		this.linerTemplate = linerTemplate;
	}
	public String getRapTemplate() {
		return rapTemplate;
	}
	public void setRapTemplate(String rapTemplate) {
		this.rapTemplate = rapTemplate;
	}
	public String getRcpTemplate() {
		return rcpTemplate;
	}
	public void setRcpTemplate(String rcpTemplate) {
		this.rcpTemplate = rcpTemplate;
	}
	

}
